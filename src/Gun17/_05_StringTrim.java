package Gun17;

public class _05_StringTrim {

    public static void main(String[] args) {

        // trim: Stringin başındaki ve sonundaki boşlukları alır

        String text="   Merhana Dünya    ";
        System.out.println("Orjinal hali = ->" + text+"<-");
        System.out.println("trimli hali = ->" + text.trim() + "<-");

    }

}
