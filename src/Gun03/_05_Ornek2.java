package Gun03;

public class _05_Ornek2 {
    public static void main(String[] args) {

        // Yari capi 4.2 olan bir dairenin cevresini ve alanını bulunuz
        // cevre = 2*yaricap*3.14; alan=3.14*yaricap*yaricap

        double yaricap=4.2; // hepsini burda tanımlayıp diger tanimlari altta düz olarak kullanabiliriz
        double cevre=2*yaricap*3.14;
        double alan=3.14*yaricap*yaricap;

        System.out.println("cevre = " + cevre+ "cm");
        System.out.println("alan = " + alan+ "cm2" );
    }

}
